from __future__ import annotations

# Keep descriptor bytes in HID item rows to match the Adafruit HID descriptor
# style and make item boundaries easier to review.
# fmt: off
DEFAULT_KEYBOARD_DESCRIPTOR = bytes(
    (
        0x05, 0x01,  # Usage Page (Generic Desktop)
        0x09, 0x06,  # Usage (Keyboard)
        0xA1, 0x01,  # Collection (Application)
          0x05, 0x07,  # Usage Page (Keyboard)
          0x19, 0xE0,  # Usage Minimum (Keyboard LeftControl modifier bit)
          0x29, 0xE7,  # Usage Maximum (Keyboard Right GUI modifier bit)
          0x15, 0x00,  # Logical Minimum (modifier bit released)
          0x25, 0x01,  # Logical Maximum (modifier bit pressed)
          0x75, 0x01,  # Report Size (1 bit per modifier)
          0x95, 0x08,  # Report Count (8 modifier usages -> byte 0)
          0x81, 0x02,  # Input (modifier byte: Data, Variable, Absolute)
          0x95, 0x01,  # Report Count (1 reserved boot-keyboard byte)
          0x75, 0x08,  # Report Size (8 bits for byte 1)
          0x81, 0x01,  # Input (reserved byte: Constant)
          0x95, 0x03,  # Report Count (3 keyboard LED status bits)
          0x75, 0x01,  # Report Size (1 bit per LED)
          0x05, 0x08,  # Usage Page (LEDs)
          0x19, 0x01,  # Usage Minimum (Num Lock)
          0x29, 0x03,  # Usage Maximum (Scroll Lock)
          0x91, 0x02,  # Output (host-to-device LED bitfield)
          0x95, 0x05,  # Report Count (5 LED padding bits)
          0x75, 0x01,  # Report Size (1 bit per padding slot)
          0x91, 0x01,  # Output (LED padding: Constant)
          0x95, 0x06,  # Report Count (6 simultaneous key usage slots)
          0x75, 0x08,  # Report Size (8 bits per key usage)
          0x15, 0x00,  # Logical Minimum (no key / usage 0)
          0x26, 0xFF, 0x00,  # Logical Maximum (255, one-byte key usages)
          0x05, 0x07,  # Usage Page (Keyboard, for key array entries)
          0x19, 0x00,  # Usage Minimum (No event)
          0x2A, 0xFF, 0x00,  # Usage Maximum (255)
          0x81, 0x00,  # Input (6-key array: Data, Array)
        0xC0,  # End Collection
    )
)

DEFAULT_MOUSE_DESCRIPTOR = bytes(
    (
        0x05, 0x01,  # Usage Page (Generic Desktop)
        0x09, 0x02,  # Usage (Mouse)
        0xA1, 0x01,  # Collection (Application)
          0x09, 0x01,  # Usage (Pointer)
          0xA1, 0x00,  # Collection (Physical)
            0x05, 0x09,  # Usage Page (Button)
            0x19, 0x01,  # Usage Minimum (Button 1)
            0x29, 0x08,  # Usage Maximum (Button 8)
            0x15, 0x00,  # Logical Minimum (button released)
            0x25, 0x01,  # Logical Maximum (button pressed)
            0x95, 0x08,  # Report Count (8 button usages -> byte 0)
            0x75, 0x01,  # Report Size (1 bit per button)
            0x81, 0x02,  # Input (button bitfield: Data, Variable, Absolute)
            0x05, 0x01,  # Usage Page (Generic Desktop)
            0x09, 0x30,  # Usage (X)
            0x09, 0x31,  # Usage (Y)
            0x16, 0x01,  # Logical Minimum (-32767)
            0x80, 0x26,  # Logical Minimum continuation, Logical Maximum
            0xFF, 0x7F,  # Logical Maximum continuation (32767)
            0x75, 0x10,  # Report Size (16 bits per X/Y axis)
            0x95, 0x02,  # Report Count (2 axes -> bytes 1..4)
            0x81, 0x06,  # Input (signed relative X/Y movement)
            0xA1, 0x02,  # Collection (Logical)
              0x09, 0x48,  # Usage (Resolution Multiplier for vertical wheel)
              0x15, 0x00,  # Logical Minimum (0)
              0x25, 0x01,  # Logical Maximum (1)
              0x35, 0x01,  # Physical Minimum (1)
              0x45, 0x08,  # Physical Maximum (8)
              0x75, 0x02,  # Report Size (2)
              0x95, 0x01,  # Report Count (1)
              0xB1, 0x02,  # Feature (host-visible multiplier selector)
              0x75, 0x06,  # Report Size (6 feature padding bits)
              0x95, 0x01,  # Report Count (1 padding field)
              0xB1, 0x01,  # Feature (multiplier padding: Constant)
              0x09, 0x38,  # Usage (Wheel)
              0x15, 0x81,  # Logical Minimum (-127)
              0x25, 0x7F,  # Logical Maximum (127)
              0x35, 0x00,  # Physical Minimum (0)
              0x45, 0x00,  # Physical Maximum (0)
              0x75, 0x08,  # Report Size (8-bit signed vertical wheel)
              0x95, 0x01,  # Report Count (1 wheel field -> byte 5)
              0x81, 0x06,  # Input (relative vertical wheel)
            0xC0,  # End Collection
            0x05, 0x0C,  # Usage Page (Consumer)
            0xA1, 0x02,  # Collection (Logical)
              0x05, 0x01,  # Usage Page (Generic Desktop)
              0x09, 0x48,  # Usage (Resolution Multiplier for horizontal pan)
              0x15, 0x00,  # Logical Minimum (0)
              0x25, 0x01,  # Logical Maximum (1)
              0x35, 0x01,  # Physical Minimum (1)
              0x45, 0x08,  # Physical Maximum (8)
              0x75, 0x02,  # Report Size (2)
              0x95, 0x01,  # Report Count (1)
              0xB1, 0x02,  # Feature (host-visible multiplier selector)
              0x75, 0x06,  # Report Size (6 feature padding bits)
              0x95, 0x01,  # Report Count (1 padding field)
              0xB1, 0x01,  # Feature (multiplier padding: Constant)
              0x05, 0x0C,  # Usage Page (Consumer)
              0x0A, 0x38, 0x02,  # Usage (AC Pan)
              0x15, 0x81,  # Logical Minimum (-127)
              0x25, 0x7F,  # Logical Maximum (127)
              0x35, 0x00,  # Physical Minimum (0)
              0x45, 0x00,  # Physical Maximum (0)
              0x75, 0x08,  # Report Size (8-bit signed horizontal pan)
              0x95, 0x01,  # Report Count (1 pan field -> byte 6)
              0x81, 0x06,  # Input (relative horizontal pan)
            0xC0,  # End Collection
          0xC0,  # End Collection
        0xC0,  # End Collection
    )
)
